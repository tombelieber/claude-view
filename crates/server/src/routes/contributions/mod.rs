    async fn do_get(app: axum::Router, uri: &str) -> (StatusCode, String, HeaderMap) {
        let response = app
            .oneshot(Request::builder().uri(uri).body(Body::empty()).unwrap())
            .await
            .unwrap();
        let status = response.status();
        let headers = response.headers().clone();
        let body = axum::body::to_bytes(response.into_body(), usize::MAX)
            .await
            .unwrap();
        (status, String::from_utf8(body.to_vec()).unwrap(), headers)
    }

    async fn insert_contribution_session(
        db: &Database,
        id: &str,
        last_message_at: i64,
        is_sidechain: bool,
        branch: Option<&str>,
    ) {
        // Insert into sessions (legacy table for backwards compat)
        db.pool()
            .execute(
                sqlx::query(
                    r#"
                    INSERT INTO sessions (
                        id, project_id, file_path, preview, project_path, project_display_name,
                        duration_seconds, files_edited_count, reedited_files_count, files_read_count,
                        user_prompt_count, api_call_count, tool_call_count, commit_count, turn_count,
                        last_message_at, size_bytes, last_message, files_touched, skills_used,
                        files_read, files_edited, ai_lines_added, ai_lines_removed,
                        is_sidechain, git_branch
                    )
                    VALUES (
                        ?1, 'proj', '/tmp/' || ?1 || '.jsonl', 'test', '/tmp', 'Project',
                        120, 2, 0, 1,
                        3, 5, 7, 0, 4,
                        ?2, 1024, 'last', '[]', '[]',
                        '[]', '[]', 10, 2,
                        ?3, ?4
                    )
                    "#,
                )
                .bind(id)
                .bind(last_message_at)
                .bind(is_sidechain)
                .bind(branch),
            )
            .await
            .unwrap();

        // Also insert into session_stats (primary read table for Phase 7.c queries)
        db.pool()
            .execute(
                sqlx::query(
                    r#"
                    INSERT INTO session_stats (
                        session_id, source_content_hash, source_size, parser_version,
                        stats_version, indexed_at, last_message_at, is_sidechain,
                        git_branch
                    )
                    VALUES (
                        ?1, X'00', 1024, 1, 3, 0, ?2, ?3, ?4
                    )
                    "#,
                )
                .bind(id)
                .bind(last_message_at)
                .bind(is_sidechain as i64)
                .bind(branch),
            )
            .await
            .unwrap();
    }

    #[tokio::test]
    async fn test_get_contributions_empty_db() {
        let db = test_db().await;
        let app = build_app(db);
        let (status, body, headers) = do_get(app, "/api/contributions").await;

        assert_eq!(status, StatusCode::OK);

        // Check response structure
        let json: serde_json::Value = serde_json::from_str(&body).unwrap();
        assert!(json["overview"].is_object());
        assert!(json["overview"]["fluency"].is_object());
        assert!(json["overview"]["output"].is_object());
        assert!(json["overview"]["effectiveness"].is_object());
        assert!(json["trend"].is_array());
        assert!(json["efficiency"].is_object());
        assert!(json["byBranch"].is_array());

        // Empty responses should not be cached (server may still be generating snapshots)
        let cache_control = headers.get("cache-control").unwrap().to_str().unwrap();
        assert!(cache_control.contains("no-cache"));
    }


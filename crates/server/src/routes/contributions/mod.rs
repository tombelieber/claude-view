// crates/server/src/routes/contributions/mod.rs
//! Contributions API endpoints for Theme 3.
//!
//! This module provides:
//! - `GET /api/contributions` - Main contributions page data
//! - `GET /api/contributions/sessions/:id` - Session contribution detail
//! - `GET /api/contributions/branches/:name/sessions` - Sessions for a branch
//!
//! ## Caching
//!
//! Responses include Cache-Control headers based on the time range:
//! - Today: 1 minute (real-time data)
//! - Week: 5 minutes
//! - Month: 15 minutes
//! - 90 days / All: 30 minutes

mod handlers;
mod helpers;
mod scope_meta;
mod types;

use std::sync::Arc;

use axum::{routing::get, Router};

use crate::state::AppState;

// Re-export handler functions (used in openapi.rs path annotations)
pub use handlers::{get_branch_sessions, get_contributions, get_session_contribution};

// Re-export utoipa __path_* types (generated by #[utoipa::path] on handlers)
pub use handlers::{
    __path_get_branch_sessions, __path_get_contributions, __path_get_session_contribution,
};

// Re-export response/query types (used in openapi.rs schema registration)
pub use types::{
    BranchSessionsResponse, ContributionWarning, ContributionsResponse, EffectivenessMetrics,
    EfficiencyMetrics, FluencyMetrics, OutputMetrics, OverviewMetrics, SessionContributionResponse,
};

/// Create the contributions routes router.
pub fn router() -> Router<Arc<AppState>> {
    Router::new()
        .route("/contributions", get(get_contributions))
        .route(
            "/contributions/sessions/{id}",
            get(get_session_contribution),
        )
        .route(
            "/contributions/branches/{name}/sessions",
            get(get_branch_sessions),
        )
}

